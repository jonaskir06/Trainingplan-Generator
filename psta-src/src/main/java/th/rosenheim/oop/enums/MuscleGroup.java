package th.rosenheim.oop.enums;

/**
 * Beschreibt die Hauptmuskelgruppen, die beim Krafttraining
 * trainiert werden können. Dadurch kann man Trainings strukturieren und somit
 * Überbelastung durch aufeinanderfolgende Trainingstage vermeiden.
 */
public enum MuscleGroup {

    /** Brustmuskulatur */
    CHEST,

    /** Rückenmuskulatur */
    BACK,

    /** Beinmuskulatur */
    LEGS,

    /** Armmuskulatur (Bizeps, Trizeps) */
    ARMS,

    /** Schultermuskulatur */
    SHOULDERS,

    /** Bauchmuskulatur */
    CORE
}