package th.rosenheim.oop.enums;

/**
 * Beschreibt den Schwierigkeitsgrad eines Workouts oder einer Übung.
 * Der Schwierigkeitsgrad beeinflusst unter anderem die Intensität,
 * die Anzahl der Wiederholungen sowie die Komplexität der Übungen.
 */
public enum DifficultyLevel {

    /** Leichtes Trainingsniveau, geeignet für Einsteiger */
    EASY,

    /** Mittleres Trainingsniveau für Fortgeschrittene */
    MEDIUM,

    /** Hohes Trainingsniveau für sehr erfahrene User */
    HARD,
}
